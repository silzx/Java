package com.zl.util;

import java.util.UUID;
public final class Uuid {
	
	private Uuid() {}
	

	/**   
	 * @Title: getUuid   
	 * @author：汪泽轩
	 * @Description: 生成唯一序列               
	 */
	public static String getUuid() {
		String uuid= UUID.randomUUID().toString().replaceAll("-", "");
		return  uuid;
	}
}
